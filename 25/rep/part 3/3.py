c = 0
n = 650001
while c != 6:
    s = 0
    for i in range(2, int(n**0.5)+1):
        if i * i == n:
            s += i
        elif n % i == 0:
            s += i + n//i
    if s % 17 == 0:
        print(n, int(s / 17))
        c += 1
    n += 1

