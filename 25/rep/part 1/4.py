for i in range(144120, 178204):
    nums = []
    # Так как нужно 25 делителей (нечетное кол-во), то берем только такие числа, у которых есть корни
    if i**0.5 != int(i**0.5):
        continue
    for j in range(2, int(i**0.5) + 1):
        if i % j == 0:
            nums.append(j)
            nums.append(i//j)
    if len(set(nums)) == 25:
        print(i, max(nums) + min(nums))
