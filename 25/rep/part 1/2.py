for i in range(121000, 121452+1):
    nums = [i]
    for j in range(2, i//2 + 1, 2):  # Берем четные числа сразу, идем до i//2 для оптимицации
        if i % j == 0:
            nums.append(j)
        if len(nums) > 10:
            break
    if len(nums) == 10:
        print(nums)
